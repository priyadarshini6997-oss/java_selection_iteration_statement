package com.index;

public class Iteration_white {

	public static void main(String[] args) {
		int i=1;
		System.out.println("Incrementing while loop");
		while(i<=5) {
			System.out.println(i);
			i++;
			}
		
		System.out.println("Decrementing while loop");
		while(i>=1) {
			System.out.println(i);
			i--;
			}

	}

}
