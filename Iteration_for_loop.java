package com.index;

public class Iteration_for_loop {

	public static void main(String[] args) {
		System.out.println("Printing 0 to 10");
		for(int i=0;i<=10;i++)
		{
			System.out.println(i);
		}
		System.out.println("Printing 10 to 0");
		for(int i=10;i>=0;i--)
		{
			System.out.println(i);
		}

	}

}
